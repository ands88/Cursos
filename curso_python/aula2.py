print(12, 34, 1011, sep=' ', end="##\r\n") # a função print requer um argumento, para mais argumentos num mesmo print, coloca-se vírgula
""" comando sep, cria um novo separador entre os argumentos"""
print (56, 78, sep="-", end="\n##") # argumentos não nomeados
print (9, 10, 11, sep="-", end="##") # argumentos não nomeados
# \r\n -> CRLF - quebra de linha no windows
# \n -> LF - quebra de linha no unix