print(1234)

# Aspas simples
print('Anderson Ramos')
print(1, 'Anderson "Ramos"')

# Aspas duplas
print("Anderson Ramos")
print(2, "Anderson 'Ramos'")

# Escape - utiliza a barra invertida para que o próximo caracter não seja lido
print("Anderson \"Ramos")

# r - utilizado para expressões regulares
print(r"Anderson \"Ramos\"")